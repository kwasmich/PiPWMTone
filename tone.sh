#!/bin/bash

tone2init() {
  gpio -g mode 18 pwm
  gpio pwm-ms
}

tone2() {
  local note="$1" time="$2"
  local period="$(perl -e"printf'%.0f',600000/$note")"
  echo ${period}
  gpio pwmr "$((period))"
  gpio -g pwm 18 "$((period/2))"
  sleep "$time"
}

tone2mute() {
  local time="$1"
  gpio -g pwm 18 0
  sleep "$time"
}

tone2destroy() {
  gpio -g mode 18 in
}


